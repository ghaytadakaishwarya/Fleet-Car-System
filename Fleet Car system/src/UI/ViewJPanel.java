private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        model.setRowCount(0);

        for (VitalInfo vih : History.getHistory()) {

            Object[] row = new Object[10];
            row[0] = vih;
            
            row[1] = vih.getManufacturer();
            row[2] = vih.getOccupancy();
            row[3] = vih.getManu_Certi();
            row[4] = vih.getSerial_num();
            row[5] = vih.getCity();
            if(vih.getCar_Availability() == false) {
                row[6] = "No";
            } else {
                row[6] = "Yes";
            }
            row[7] = vih.getBrand_Name();
            row[8] = vih.getMan_Date();
            row[9] = vih.getSeat_capacity();
            model.addRow(row);
        }
    }
