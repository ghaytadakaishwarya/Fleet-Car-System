        System.out.println(String.valueOf(selectedDetails.getCar_Availability()));

        txtManufacturedYear.setText(String.valueOf(selectedDetails.getManufactured_Year()));
        txtSeatCapacity.setText(selectedDetails.getSeat_Capacity());

        System.out.println(selectedDetails.getSeat_Capacity());

        txtSerialNumber.setText(selectedDetails.getSerial_Number());
        txtModelNumber.setText(selectedDetails.getModel_Number());
        txtCity.setText(selectedDetails.getCity());

        System.out.println(selectedDetails.getCity());

        txtExpiredDate.setText(selectedDetails.getExpired_date());


